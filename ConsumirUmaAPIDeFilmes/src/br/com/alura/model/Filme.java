package br.com.alura.model;

public class Filme {
	
	 private String title;
	 private String image;
	 private String imDbRating;
	 private String year;
	
	public String getTitle() {
		return title;
	}
	public String getUrlImage() {
		return image;
	}
	public String getNota() {
		return imDbRating;
	}
	public String getAno() {
		return year;
	}

}
